    private void btnmasukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmasukActionPerformed
       try {
           Connection c = Koneksi.setKoneksi();
           Statement s = c.createStatement();
           
           String sql = "SELECT * FROM registrasi WHERE username=('"+txtuser2.getText()
                   +"') AND password=('"+jppass3.getText()+"')";
           ResultSet r = s.executeQuery(sql);
           
           if (r.next()) {
               if (txtuser2.getText().equals(r.getString("username"))&& jppass3.getText().equals(r.getString("password"))) {
               JOptionPane.showMessageDialog(null, "Login Succesfully");
                dispose();
                FormMenu fm = new FormMenu();
                fm.setVisible(true);
               }
           }else {
               JOptionPane.showMessageDialog(null, "Wrong Username or Password");
               jppass3.requestFocus();
           }
       } catch (Exception e) {
           System.out.println("Error");
       }
    }//GEN-LAST:event_btnmasukActionPerformed

