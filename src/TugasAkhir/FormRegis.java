    private void btnselesaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnselesaiActionPerformed
        if ("".equals(txtname.getText()) || "".equals(txtemail.getText())
                || "".equals(txtuser.getText()) || "".equals(txtpass.getText())) {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error",
                    JOptionPane.WARNING_MESSAGE);
        } else {
            String SQL = "INSERT INTO registrasi (namahr, email, username, password)"
                    + "VALUE('" + txtname.getText() + "','" + txtemail.getText() + "','"
                    + txtuser.getText() + "','" + txtpass.getText() + "')";
            int status = Koneksi.execute(SQL);
            if (status == 1) {
                JOptionPane.showMessageDialog(this, "Data Berhasil Ditambahkan", "Sukses",
                        JOptionPane.INFORMATION_MESSAGE);
                bersih();
                dispose();
                FormAwal fa = new FormAwal();
                fa.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Data Gagal Ditambahkan", "Sukses",
                        JOptionPane.WARNING_MESSAGE);
            }
        }
        
    }//GEN-LAST:event_btnselesaiActionPerformed

